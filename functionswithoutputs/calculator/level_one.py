def add(n1,n2):
  """This will add two numbers"""
  return n1+n2

def subtract(n1,n2):
  """This will subtract two numbers"""
  return n1-n2

def multiply(n1,n2):
  """This will multiply two numbers"""
  return n1*n2

def divide(n1,n2):
  """This will divide two numbers"""
  return n1/n2

print(add(10,345))
print(subtract(10,345))
print(multiply(10,345))
print(divide(10,345))
